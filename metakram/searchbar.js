let results = document.getElementById("results");
Object.entries(all_pages).forEach(([key,value])=>{
    let a = document.createElement("a");
    a.innerText = key;
    a.href = value;
    a.classList.add("resultItem");
    results.appendChild(a);
});

function search() {
    let resultsContainer = document.getElementById("results");
    let resultItems = document.getElementsByClassName("resultItem");

    let input = document.getElementById("searchbar").value
    input=input.toLowerCase();

    let noResults = true;
    for (i=0;i<resultItems.length;i++) {
        if (!resultItems[i].innerHTML.toLowerCase().includes(input) || input === "") {
            resultItems[i].style.display="none";
            continue;
        }
        else {
            resultItems[i].style.display="flex";
            noResults=false;
        }
    }

    resultsContainer.style.display = noResults ? "none":"block";
};

